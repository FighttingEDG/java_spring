USE interview_study;

-- ============================================
-- 聚合函数（必会）
-- ============================================

-- 【聚合函数说明】
-- COUNT：统计行数量
-- SUM：获取单个列的合计值
-- AVG：计算某个列的平均值
-- MAX：计算列的最大值
-- MIN：计算列的最小值

-- ============================================
-- 1. COUNT：统计行数量
-- ============================================

-- 【示例1】统计员工总数
SELECT '=== COUNT示例1：统计员工总数 ===' AS description;
SELECT COUNT(*) AS 员工总数
FROM employee;


-- ============================================
-- 2. SUM：获取单个列的合计值
-- ============================================

-- 【示例1】计算所有员工的工资总和
SELECT '=== SUM示例1：计算所有员工工资总和 ===' AS description;
SELECT SUM(salary) AS 工资总和
FROM employee;

-- ============================================
-- 3. AVG：计算某个列的平均值
-- ============================================

-- 【示例1】计算员工平均工资
SELECT '=== AVG示例1：计算员工平均工资 ===' AS description;
SELECT AVG(salary) AS 平均工资
FROM employee;

-- ============================================
-- 4. MAX：计算列的最大值
-- ============================================

-- 【示例1】找出最高工资
SELECT '=== MAX示例1：找出最高工资 ===' AS description;
SELECT MAX(salary) AS 最高工资
FROM employee;

-- ============================================
-- 5. MIN：计算列的最小值
-- ============================================

-- 【示例1】找出最低工资
SELECT '=== MIN示例1：找出最低工资 ===' AS description;
SELECT MIN(salary) AS 最低工资
FROM employee;
